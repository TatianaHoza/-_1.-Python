enter = ' '
T = []
i = 0
res = {'название': [], 'цена': [], 'количество': [], 'единицы измерения': []}
t = ('название', 'цена', 'количество', 'единицы измерения')
while enter:
    i = i + 1
    a = input("\nВведите название товара:")
    b = input("Введите цену:")
    c = input("Введите количество:")
    d = input("Введите единицы измерения: ")
    T.append((i, {'название': a, 'цена': b, 'количество': c, 'единицы': d}))
    print('\n', T)
    enter = input("Повторить ещё раз? ")

    for id, v in T:
        res["название"].append(["a"])
