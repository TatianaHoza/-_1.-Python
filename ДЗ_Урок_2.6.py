    while True:
        print("\nВыберите действие:")
        print("[1] Выведите список названий товара")
        print("[2] Выведите список названий цен")
        print("[3] Выведите список количества товара")
        print("[4] Выведите список единиц измерения")
        print("[5] Закончите действие")
        e = input("\nВыберите действие:")
        if e == '5':
            break
    print(f"\n{int(e)-1}:{res[t[int(e)-1]]}")